int	ft_str_is_uppercase(char *str)
{
	int i;
	int is_min;

	is_min = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] < 'A' || str[i] > 'Z')
			is_min = 0;
		i++;
	}
	return (is_min);
}
