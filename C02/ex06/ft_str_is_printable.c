int	ft_str_is_printable(char *str)
{
	int i;
	int is_prt;

	is_prt = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] < 32 || str[i] > 126)
			is_prt = 0;
		i++;
	}
	return (is_prt);
}
